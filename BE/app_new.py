def _get_foot_point(box) -> tuple:
    """Return the bottom-center pixel of a bounding box (player's foot)."""
    x1, y1, x2, y2 = box
    return (int((x1 + x2) / 2), int(y2))


def process_video(input_path: str, output_path: str, mode: str,
                  test_mode: bool, conf_map: dict):
    """
    Main processing loop.
    Replaces the original SwishAI predict loop with model.track() so every
    detected player gets a stable ByteTrack ID across frames.
    """
    global processing_state

    # ---- Load homography (optional — minimap disabled if missing) ----
    H = None
    if HOMOGRAPHY_PATH.exists():
        H = np.load(str(HOMOGRAPHY_PATH))
        print(f"[SwishAI] Homography loaded from {HOMOGRAPHY_PATH}")
    else:
        print("[SwishAI] WARNING: homography.npy not found. "
              "Run calibrate.py first. Minimap will be disabled.")

    cap = cv2.VideoCapture(input_path)
    if not cap.isOpened():
        raise RuntimeError(f"Cannot open video: {input_path}")

    fps        = cap.get(cv2.CAP_PROP_FPS) or 30.0
    src_w      = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
    src_h      = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))
    total_f    = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))

    if test_mode:
        max_frames = int(fps * 15)
        total_f    = min(total_f, max_frames)
    else:
        max_frames = total_f

    # ---- Output frame layout ----
    # Left:  original video feed  (src_w x src_h)
    # Right: stats panel on top   (PANEL_W x panel_top_h)
    #        minimap on bottom     (MAP_W   x MAP_H)
    #
    # Right column width = max(PANEL_W, MAP_W)
    RIGHT_W   = max(PANEL_W, MAP_W)
    panel_h   = src_h - MAP_H          # stats panel gets the remaining height
    if panel_h < 80:
        # If video is very short, split equally
        panel_h = src_h // 2
    out_w     = src_w + RIGHT_W
    out_h     = src_h

    fourcc = cv2.VideoWriter_fourcc(*"mp4v")
    writer = cv2.VideoWriter(output_path, fourcc, fps, (out_w, out_h))

    # ---- State ----
    player_stats: dict[int, dict] = defaultdict(lambda: {"shots": 0, "scores": 0})
    total_shots   = 0
    total_scores  = 0
    pulse_effects = []

    # Cooldown tracking (same logic as original SwishAI)
    SHOT_COOLDOWN_FRAMES   = int(fps * 1.5)
    BASKET_COOLDOWN_FRAMES = int(fps * 2.0)
    last_shot_frame:   dict[int, int] = {}   # player_id → last shot frame
    last_basket_frame: int            = -BASKET_COOLDOWN_FRAMES

    basket_center: tuple | None = None   # updated every frame
    frame_idx = 0

    court_base = draw_court_base()   # static base image for minimap

    while frame_idx < max_frames:
        ret, frame = cap.read()
        if not ret:
            break

        # ----------------------------------------------------------------
        # YOLO inference with ByteTrack
        # ----------------------------------------------------------------
        results = model.track(
            frame,
            persist=True,             # keeps track IDs stable across frames
            tracker="bytetrack.yaml", # use ByteTrack (bundled with Ultralytics)
            verbose=False,
            conf=min(conf_map.values()),   # broad pass; we filter per-class below
        )

        res = results[0]

        # Parse detections
        boxes      = res.boxes.xyxy.cpu().numpy()   if res.boxes is not None else []
        class_ids  = res.boxes.cls.cpu().numpy().astype(int) if res.boxes is not None else []
        confs      = res.boxes.conf.cpu().numpy()   if res.boxes is not None else []
        track_ids  = (res.boxes.id.cpu().numpy().astype(int)
                      if res.boxes is not None and res.boxes.id is not None
                      else np.full(len(boxes), -1, dtype=int))

        # ----------------------------------------------------------------
        # Build per-class detection lists (applying per-class conf filter)
        # ----------------------------------------------------------------
        players:        list[tuple] = []   # (box, track_id)
        shooter_boxes:  list[tuple] = []   # (box, track_id)
        ball_boxes:     list        = []
        basket_boxes:   list        = []
        ball_in_basket: bool        = False

        for box, cid, conf, tid in zip(boxes, class_ids, confs, track_ids):
            if conf < conf_map.get(cid, 0.5):
                continue
            if cid == CLASS_PLAYER:
                players.append((box, tid))
            elif cid == CLASS_PLAYER_SHOOT:
                shooter_boxes.append((box, tid))
            elif cid == CLASS_BALL:
                ball_boxes.append(box)
            elif cid == CLASS_BASKET:
                basket_boxes.append(box)
            elif cid == CLASS_BALL_IN_BASKET:
                ball_in_basket = True

        # Update basket center (used for pulse FX)
        if basket_boxes:
            bx1, by1, bx2, by2 = basket_boxes[0]
            basket_center = (int((bx1 + bx2) / 2), int((by1 + by2) / 2))

        # ----------------------------------------------------------------
        # Shot detection — attribute to nearest tracked player
        # ----------------------------------------------------------------
        for shoot_box, shoot_tid in shooter_boxes:
            # Resolve track_id: prefer the tracker-assigned ID,
            # fall back to finding the player bbox with most overlap.
            pid = shoot_tid if shoot_tid >= 0 else _nearest_player_id(
                shoot_box, players)

            if pid < 0:
                continue

            last_f = last_shot_frame.get(pid, -SHOT_COOLDOWN_FRAMES)
            if frame_idx - last_f >= SHOT_COOLDOWN_FRAMES:
                player_stats[pid]["shots"] += 1
                total_shots += 1
                last_shot_frame[pid] = frame_idx

        # ----------------------------------------------------------------
        # Score detection
        # ----------------------------------------------------------------
        if ball_in_basket:
            if frame_idx - last_basket_frame >= BASKET_COOLDOWN_FRAMES:
                last_basket_frame = frame_idx
                total_scores += 1

                # Attribute score to the player who shot most recently
                scorer = _find_recent_shooter(
                    last_shot_frame, frame_idx, BASKET_COOLDOWN_FRAMES * 4)
                if scorer >= 0:
                    player_stats[scorer]["scores"] += 1

                # Pulse FX
                if basket_center:
                    pulse_effects.append(
                        PulseEffect(basket_center, max_radius=90, duration=25))

        # ----------------------------------------------------------------
        # Draw overlays onto the main frame
        # ----------------------------------------------------------------
        annotated = frame.copy()

        if mode in ("full", "stats_effects"):
            # Draw bounding boxes
            if mode == "full":
                for box, tid in players:
                    _draw_player_box(annotated, box, tid, player_stats)
                for box, tid in shooter_boxes:
                    _draw_shooter_box(annotated, box, tid)
                for box in ball_boxes:
                    _draw_ball_box(annotated, box)
                for box in basket_boxes:
                    _draw_basket_box(annotated, box)

            # Pulse effects
            for pulse in pulse_effects:
                pulse.draw(annotated)
            pulse_effects = [p for p in pulse_effects if p.active]

            # Score flash overlay when ball_in_basket
            if ball_in_basket:
                _draw_score_flash(annotated, total_scores)

        # ----------------------------------------------------------------
        # Build right-side panel
        # ----------------------------------------------------------------
        fg_pct = (total_scores / total_shots * 100) if total_shots > 0 else 0.0

        stats_panel = draw_stats_panel(
            player_stats, total_shots, total_scores, fg_pct, panel_h)

        # Minimap
        minimap = court_base.copy()
        if H is not None:
            _draw_players_on_minimap(minimap, players + shooter_boxes,
                                     H, player_stats)

        # Pad minimap to right column width
        minimap_padded = np.zeros((MAP_H, RIGHT_W, 3), dtype=np.uint8)
        minimap_padded[:, :MAP_W] = minimap

        # Pad stats panel to right column width
        stats_padded = np.zeros((panel_h, RIGHT_W, 3), dtype=np.uint8)
        stats_padded[:, :PANEL_W] = stats_panel

        # Stack stats + minimap vertically
        right_col = np.vstack([stats_padded, minimap_padded])
        # Ensure height matches
        if right_col.shape[0] != out_h:
            right_col = cv2.resize(right_col, (RIGHT_W, out_h))

        # Combine left (video) + right (panel)
        combined = np.hstack([annotated, right_col])
        writer.write(combined)

        # ---- Progress update ----
        frame_idx += 1
        progress = int((frame_idx / max_frames) * 100)
        with state_lock:
            processing_state["progress"]     = progress
            processing_state["total_shots"]  = total_shots
            processing_state["total_scores"] = total_scores
            processing_state["fg_percentage"] = fg_pct
            processing_state["player_stats"] = {
                int(k): dict(v) for k, v in player_stats.items()
            }

    cap.release()
    writer.release()

    # Final state
    with state_lock:
        processing_state["status"]      = "completed"
        processing_state["progress"]    = 100
        processing_state["output_file"] = output_path


