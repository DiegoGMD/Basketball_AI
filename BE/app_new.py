# Half-court real-world dimensions in cm (NBA standard, used for homography)
# You can change these to match your actual court if it's non-standard.
COURT_W_CM = 1500   # 15 m wide (full court width)
COURT_H_CM = 1400   # 14 m deep (half-court length)

# Minimap canvas size in pixels
MAP_W = 300
MAP_H = 280

# Scale factors: real-world cm → minimap pixels
SCALE_X = MAP_W / COURT_W_CM
SCALE_Y = MAP_H / COURT_H_CM

# Player dot colors (BGR) — cycles for up to 10 players
PLAYER_COLORS = [
    (0,   200, 255),   # yellow-ish
    (255, 100,   0),   # blue
    (0,   255, 100),   # green
    (0,    80, 255),   # red
    (255,   0, 200),   # magenta
    (0,   200,   0),   # dark green
    (200,   0, 255),   # purple
    (0,   150, 255),   # orange
    (255, 255,   0),   # cyan
    (100, 100, 255),   # salmon
]


def get_player_color(player_id: int):
    return PLAYER_COLORS[int(player_id) % len(PLAYER_COLORS)]


def draw_court_base() -> np.ndarray:
    """
    Draw a top-down half-court diagram on a dark canvas.
    All measurements scaled from real-world cm to MAP_W x MAP_H pixels.
    Returns a BGR image.
    """
    court = np.zeros((MAP_H, MAP_W, 3), dtype=np.uint8)
    court[:] = (30, 30, 30)   # dark background

    def cx(cm): return int(cm * SCALE_X)
    def cy(cm): return int(cm * SCALE_Y)

    LINE = (180, 180, 180)
    PAINT = (50, 50, 80)

    # Court boundary
    cv2.rectangle(court, (0, 0), (MAP_W - 1, MAP_H - 1), LINE, 1)

    # Paint / key (roughly 490 cm wide, 575 cm deep — NBA proportions scaled)
    paint_x1 = cx(505)
    paint_x2 = cx(995)
    cv2.rectangle(court, (paint_x1, 0), (paint_x2, cy(575)), PAINT, -1)
    cv2.rectangle(court, (paint_x1, 0), (paint_x2, cy(575)), LINE, 1)

    # Free-throw circle (radius ~180 cm)
    ft_center = (cx(750), cy(575))
    cv2.circle(court, ft_center, cx(180), LINE, 1)

    # Basket (small filled circle near top edge, centered)
    basket_pt = (cx(750), cy(157))
    cv2.circle(court, basket_pt, 8, (0, 120, 255), -1)
    cv2.circle(court, basket_pt, 8, LINE, 1)

    # Backboard line
    cv2.line(court,
             (cx(660), cy(90)), (cx(840), cy(90)),
             (200, 200, 200), 2)

    # Three-point arc — approximated as an arc + two straight side lines
    # NBA: 3pt line is 723 cm from basket center, cut-off at sidelines ~90 cm from baseline
    arc_center = basket_pt
    arc_radius = cx(723)
    cv2.ellipse(court, arc_center, (arc_radius, arc_radius),
                0, 0, 180, LINE, 1)

    # Label
    cv2.putText(court, "COURT MAP", (5, MAP_H - 6),
                cv2.FONT_HERSHEY_SIMPLEX, 0.35, (140, 140, 140), 1)

    return court


def project_to_minimap(foot_px: tuple, H: np.ndarray) -> tuple | None:
    """
    Transform a pixel point (foot of player bbox) to minimap coordinates
    using the homography matrix H.
    Returns (map_x, map_y) or None if transform fails.
    """
    try:
        pt = np.array([[[float(foot_px[0]), float(foot_px[1])]]], dtype=np.float32)
        dst = cv2.perspectiveTransform(pt, H)
        real_x, real_y = dst[0][0]   # coordinates in cm on court
        map_x = int(real_x * SCALE_X)
        map_y = int(real_y * SCALE_Y)
        if 0 <= map_x < MAP_W and 0 <= map_y < MAP_H:
            return (map_x, map_y)
        return None
    except Exception:
        return None


