@app.post("/upload")
async def upload_video(file: UploadFile = File(...)):
    """Accept a video file and save it to the uploads directory."""
    allowed = {".mp4", ".mov", ".avi", ".mkv"}
    suffix  = Path(file.filename).suffix.lower()
    if suffix not in allowed:
        raise HTTPException(400, f"Unsupported format: {suffix}")

    unique_name = f"{uuid.uuid4()}{suffix}"
    save_path   = UPLOAD_DIR / unique_name

    with open(save_path, "wb") as f:
        content = await file.read()
        f.write(content)

    return {"filename": unique_name, "message": "Upload successful"}


@app.post("/process")
def start_processing(req: ProcessRequest):
    """Start background video processing."""
    global processing_state

    with state_lock:
        if processing_state["status"] == "processing":
            raise HTTPException(409, "Already processing a video")

    input_path  = str(UPLOAD_DIR / req.filename)
    if not os.path.exists(input_path):
        raise HTTPException(404, "Uploaded file not found")

    out_name    = f"out_{uuid.uuid4()}.mp4"
    output_path = str(PROCESSED_DIR / out_name)

    conf_map = {
        CLASS_BALL:           req.conf_ball,
        CLASS_BALL_IN_BASKET: req.conf_in_basket,
        CLASS_PLAYER:         req.conf_player,
        CLASS_BASKET:         req.conf_basket,
        CLASS_PLAYER_SHOOT:   req.conf_shoot,
    }

    with state_lock:
        processing_state.update({
            "status":        "processing",
            "progress":      0,
            "output_file":   None,
            "error":         None,
            "player_stats":  {},
            "total_shots":   0,
            "total_scores":  0,
            "fg_percentage": 0.0,
        })

    t = threading.Thread(
        target=_run_processing,
        args=(input_path, output_path, req.mode, req.test_mode, conf_map),
        daemon=True,
    )
    t.start()

    return {"message": "Processing started", "output_file": out_name}


