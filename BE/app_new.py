class PulseEffect:
    """Draws an expanding ring at the basket when a score is registered."""
    def __init__(self, center: tuple, max_radius: int = 80, duration: int = 20):
        self.center     = center
        self.max_radius = max_radius
        self.duration   = duration
        self.frame      = 0

    @property
    def active(self):
        return self.frame < self.duration

    def draw(self, img: np.ndarray):
        if not self.active:
            return
        t      = self.frame / self.duration
        radius = int(t * self.max_radius)
        alpha  = int(255 * (1 - t))
        overlay = img.copy()
        cv2.circle(overlay, self.center, radius, (0, 200, 255), 3)
        cv2.addWeighted(overlay, alpha / 255, img, 1 - alpha / 255, 0, img)
        self.frame += 1


