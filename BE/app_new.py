PANEL_W = 300
PANEL_BG = (20, 20, 20)


def draw_stats_panel(player_stats: dict, total_shots: int,
                     total_scores: int, fg_pct: float,
                     panel_h: int) -> np.ndarray:
    """
    Render a dark stats panel showing:
      - Global totals row
      - Per-player table: ID | Shots | Scores | FG%
    Returns a BGR image of size (panel_h, PANEL_W).
    """
    panel = np.zeros((panel_h, PANEL_W, 3), dtype=np.uint8)
    panel[:] = PANEL_BG

    # ---- Header ----
    cv2.rectangle(panel, (0, 0), (PANEL_W, 36), (40, 40, 40), -1)
    cv2.putText(panel, "SwishAI  |  Player Stats",
                (8, 24), cv2.FONT_HERSHEY_SIMPLEX, 0.5, (255, 180, 0), 1)

    # ---- Global totals ----
    y = 55
    cv2.putText(panel, f"Total Shots : {total_shots}",
                (10, y), cv2.FONT_HERSHEY_SIMPLEX, 0.45, (200, 200, 200), 1)
    y += 20
    cv2.putText(panel, f"Total Scores: {total_scores}",
                (10, y), cv2.FONT_HERSHEY_SIMPLEX, 0.45, (200, 200, 200), 1)
    y += 20
    cv2.putText(panel, f"Overall FG% : {fg_pct:.1f}%",
                (10, y), cv2.FONT_HERSHEY_SIMPLEX, 0.45, (255, 180, 0), 1)

    # ---- Divider ----
    y += 14
    cv2.line(panel, (10, y), (PANEL_W - 10, y), (80, 80, 80), 1)
    y += 14

    # ---- Per-player table header ----
    cv2.putText(panel, "ID   Shots  Made   FG%",
                (10, y), cv2.FONT_HERSHEY_SIMPLEX, 0.4, (160, 160, 160), 1)
    y += 4
    cv2.line(panel, (10, y), (PANEL_W - 10, y), (60, 60, 60), 1)
    y += 12

    # ---- Per-player rows ----
    sorted_players = sorted(player_stats.items(), key=lambda x: -x[1]["shots"])
    for pid, stats in sorted_players:
        if y + 18 > panel_h - 10:
            break
        shots  = stats["shots"]
        scores = stats["scores"]
        pct    = (scores / shots * 100) if shots > 0 else 0.0
        color  = get_player_color(pid)
        row_txt = f"P{pid:<3}  {shots:<6} {scores:<6} {pct:.1f}%"
        cv2.putText(panel, row_txt,
                    (10, y), cv2.FONT_HERSHEY_SIMPLEX, 0.42, color, 1)
        y += 18

    return panel


