def _draw_player_box(img, box, tid, player_stats):
    x1, y1, x2, y2 = map(int, box)
    color = get_player_color(tid) if tid >= 0 else (200, 200, 200)
    cv2.rectangle(img, (x1, y1), (x2, y2), color, 2)
    stats = player_stats.get(tid, {"shots": 0, "scores": 0})
    label = (f"P{tid}  {stats['scores']}/{stats['shots']}"
             if tid >= 0 else "Player")
    _draw_label(img, label, (x1, y1), color)


def _draw_shooter_box(img, box, tid):
    x1, y1, x2, y2 = map(int, box)
    color = (0, 80, 255)
    cv2.rectangle(img, (x1, y1), (x2, y2), color, 2)
    label = f"P{tid} SHOOT" if tid >= 0 else "SHOOTING"
    _draw_label(img, label, (x1, y1), color)


def _draw_ball_box(img, box):
    x1, y1, x2, y2 = map(int, box)
    cv2.rectangle(img, (x1, y1), (x2, y2), (0, 165, 255), 2)
    _draw_label(img, "Ball", (x1, y1), (0, 165, 255))


def _draw_basket_box(img, box):
    x1, y1, x2, y2 = map(int, box)
    cv2.rectangle(img, (x1, y1), (x2, y2), (0, 255, 200), 2)
    _draw_label(img, "Basket", (x1, y1), (0, 255, 200))


def _draw_label(img, text, pt, color):
    x, y = pt
    (tw, th), _ = cv2.getTextSize(text, cv2.FONT_HERSHEY_SIMPLEX, 0.45, 1)
    y_txt = max(y - 5, th + 4)
    cv2.rectangle(img, (x, y_txt - th - 4), (x + tw + 4, y_txt + 2),
                  (0, 0, 0), -1)
    cv2.putText(img, text, (x + 2, y_txt),
                cv2.FONT_HERSHEY_SIMPLEX, 0.45, color, 1)


def _draw_score_flash(img, total_scores):
    h, w = img.shape[:2]
    overlay = img.copy()
    cv2.rectangle(overlay, (0, 0), (w, h), (0, 200, 255), -1)
    cv2.addWeighted(overlay, 0.08, img, 0.92, 0, img)
    txt = f"SCORE!  {total_scores}"
    (tw, th), _ = cv2.getTextSize(txt, cv2.FONT_HERSHEY_SIMPLEX, 1.8, 3)
    cv2.putText(img, txt,
                ((w - tw) // 2, (h + th) // 2),
                cv2.FONT_HERSHEY_SIMPLEX, 1.8, (0, 200, 255), 3)


def _draw_players_on_minimap(minimap, player_list, H, player_stats):
    for box, tid in player_list:
        foot = _get_foot_point(box)
        map_pt = project_to_minimap(foot, H)
        if map_pt is None:
            continue
        color = get_player_color(tid) if tid >= 0 else (200, 200, 200)
        cv2.circle(minimap, map_pt, 7, color, -1)
        cv2.circle(minimap, map_pt, 7, (255, 255, 255), 1)
        if tid >= 0:
            cv2.putText(minimap, f"P{tid}",
                        (map_pt[0] + 8, map_pt[1] + 4),
                        cv2.FONT_HERSHEY_SIMPLEX, 0.35,
                        color, 1)


def _nearest_player_id(shoot_box, players: list) -> int:
    """Find the track_id of the player bbox with highest IoU to shoot_box."""
    if not players:
        return -1
    best_iou = 0.0
    best_tid = -1
    sx1, sy1, sx2, sy2 = shoot_box
    for pbox, ptid in players:
        px1, py1, px2, py2 = pbox
        ix1 = max(sx1, px1); iy1 = max(sy1, py1)
        ix2 = min(sx2, px2); iy2 = min(sy2, py2)
        inter = max(0, ix2 - ix1) * max(0, iy2 - iy1)
        if inter == 0:
            continue
        union = ((sx2-sx1)*(sy2-sy1) + (px2-px1)*(py2-py1) - inter)
        iou   = inter / union if union > 0 else 0
        if iou > best_iou:
            best_iou = iou
            best_tid = ptid
    return best_tid


def _find_recent_shooter(last_shot_frame: dict, current_frame: int,
                          window: int) -> int:
    """Return the player_id with the most recent shot within `window` frames."""
    best_pid   = -1
    best_frame = -1
    for pid, f in last_shot_frame.items():
        if current_frame - f <= window and f > best_frame:
            best_frame = f
            best_pid   = pid
    return best_pid


# ===========================================================================
#  BACKGROUND WORKER THREAD
# ===========================================================================

def _run_processing(input_path: str, output_path: str, mode: str,
                    test_mode: bool, conf_map: dict):
    global processing_state
    try:
        process_video(input_path, output_path, mode, test_mode, conf_map)
    except Exception as e:
        tb = traceback.format_exc()
        print(f"[SwishAI] Processing error:\n{tb}")
        with state_lock:
            processing_state["status"] = "error"
            processing_state["error"]  = str(e)
    finally:
        # Auto-cleanup uploads
        try:
            os.remove(input_path)
        except OSError:
            pass


